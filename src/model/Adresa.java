package model;

public class Adresa {
	
	private String ulica;
	private String broj;
	private String grad;
	private String drava;
	
	public Adresa(String ulica, String broj, String grad, String drava) {
		super();
		this.ulica = ulica;
		this.broj = broj;
		this.grad = grad;
		this.drava = drava;
	}

	public Adresa() {
		super();
	}

}
